<script setup name="InspectionHistory">
import {
  Search,
  Refresh,
  Plus,
} from '@element-plus/icons-vue';

import {
  listManage,
  listOrder,
  getOrder,
  addOrder,
  updateOrder,
} from './api';

const { proxy } = getCurrentInstance();
// data
const projects = reactive({
  loading: false,
  data: [],
  map: {},
});
const params = reactive({
  pageSize: 10,
  pageNum: 1,
  projectId: null,
  projectQuantity: null,
});
const showSearch = ref(true);
const list = reactive({
  loading: false,
  total: 0,
  data: [],
});
const dialog = reactive({
  loading: false,
  visible: false,
  title: '',
  form: {},
});
const rules = {
  projectId: [
    { required: true, message: '请选择项目', trigger: 'change' },
  ],
};

// methods
const getProjects = () => {
  projects.loading = true;
  projects.data = [];
  projects.map = {};
  listManage()
    .then((res) => {
      const { rows } = res || {};
      (rows || []).forEach((item) => {
        const { id, name } = item || {};
        if (id) projects.map[id] = item;
        projects.data.push({ value: id, label: name });
      });
    })
    .catch()
    .finally(() => {
      projects.loading = false;
    });
};

const getData = () => {
  list.loading = true;
  listOrder(params)
    .then((res) => {
      const { total, rows } = res || {};
      list.total = total || 0;
      list.data = rows || [];
    })
    .catch()
    .finally(() => {
      list.loading = false;
    });
}

const paramsReset = () => {
  params.projectId = null;
  params.projectQuantity = null;
  getData();
};

const formReset = () => {
  dialog.form = {
    id: null,
    projectId: null,
    name: null,
    brand: null,
    manufacturer: null,
  };
  proxy.resetForm();
};

const handleAdd = () => {
  formReset();
  dialog.title = '新增';
  dialog.visible = true;
};

const handleDetail = (row) => {
  const { id } = row || {};
  if (id) proxy.$router.push({ path: 'process', query: { id }});
};

const handleUpdate = (row) => {
  formReset();
  dialog.loading = true;
  getOrder(row.id)
    .then((res) => {
      const { data } = res || {};
      dialog.form = data;
      dialog.title = '修改';
      dialog.visible = true;
    })
    .catch()
    .finally(() => {
      dialog.loading = false;
    });
};

const handleProjectChange = (val) => {
  const { map } = projects;
  const { name } = map[val] || {};
  dialog.form.name = name || null;
};

const handleSubmit = () => {
  proxy.$refs.form.validate((valid) => {
    if (valid) {
      dialog.loading = true;
      const { form, title } = dialog;
      const { id } = form;
      const api = id !== null ? updateOrder : addOrder;
      api(form)
        .then(() => {
          proxy.$modal.msgSuccess(`${title}成功`);
        })
        .catch()
        .finally(() => {
          dialog.loading = false;
          dialog.visible = false;
          getData();
        });
    }
  });
};

getProjects();
getData();
</script>

<template>
  <div :class="['app-container', $s.Root]">
    <el-form
      ref="queryForm"
      v-show="showSearch"
      :model="params"
      inline
      label-width="68px"
    >
      <el-form-item prop="projectId" label="产品">
        <el-select-v2
          v-model="params.projectId"
          :options="projects.data"
          clearable
          filterable
          placeholder="请选择产品"
          style="width: 300px;"
        />
      </el-form-item>

      <!-- <el-form-item prop="projectQuantity" label="检测数量">
        <el-input-number
          v-model="params.projectQuantity"
          :min="0"
          :step="1"
          step-strictly
        />
      </el-form-item> -->

      <el-form-item>
        <el-button
          type="primary"
          :icon="Search"
          @click="getData"
        >
          搜索
        </el-button>

        <el-button :icon="Refresh" @click="paramsReset">重置</el-button>
      </el-form-item>
    </el-form>

    <el-row :gutter="10" class="mb8">
      <el-col :span="1.5">
        <el-button
          type="primary"
          plain
          :icon="Plus"
          size="small"
          @click="handleAdd"
        >
          新增
        </el-button>
      </el-col>

      <right-toolbar v-model:show-search="showSearch" @query-table="getData" />
    </el-row>

    <el-table
      v-loading="list.loading"
      :data="list.data"
      border
    >
      <el-table-column
        prop="name"
        label="产品名称"
        align="center"
      />

      <el-table-column
        prop="brand"
        label="品牌"
        align="center"
      />

      <el-table-column
        prop="manufacturer"
        label="生产商"
        align="center"
      />

      <el-table-column label="操作" align="right">
        <template #default="{ row }">
          <el-button
            type="primary"
            text
            @click="handleDetail(row)"
          >
            详情
          </el-button>

          <el-button
            type="primary"
            text
            @click="handleUpdate(row)"
          >
            修改
          </el-button>
        </template>
      </el-table-column>
    </el-table>

    <pagination
      v-show="list.total > 0"
      :total="list.total"
      v-model:page="params.pageNum"
      v-model:limit="params.pageSize"
      @pagination="getData"
    />

    <el-dialog
      v-model="dialog.visible"
      :title="dialog.title"
      width="30%"
    >
      <el-form
        ref="form"
        v-loading="dialog.loading"
        :model="dialog.form"
        :rules="rules"
        label-width="80px"
      >
        <el-form-item prop="projectId" label="产品">
          <el-select-v2
            v-model="dialog.form.projectId"
            :disabled="dialog.form.id !== null"
            :options="projects.data"
            filterable
            @change="handleProjectChange"
            style="width: 300px;"
          />
        </el-form-item>

        <el-form-item prop="brand" label="品牌">
          <el-input
            v-model="dialog.form.brand"
            placeholder="请输入品牌"
            style="width: 300px;"
          />
        </el-form-item>

        <el-form-item prop="manufacturer" label="生产商">
          <el-input
            v-model="dialog.form.manufacturer"
            placeholder="请输入生产商"
            style="width: 300px;"
          />
        </el-form-item>
      </el-form>

      <template #footer>
        <el-button @click="dialog.visible = false">取消</el-button>

        <el-button
          type="primary"
          :loading="dialog.loading"
          @click="handleSubmit"
        >
          确定
        </el-button>
      </template>
    </el-dialog>
  </div>
</template>

<style lang="scss" module="$s">
